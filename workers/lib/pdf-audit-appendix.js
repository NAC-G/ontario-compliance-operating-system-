/**
 * Audit-Trail Appendix builder.
 *
 * HARD RULE: Every locked PDF must include this appendix.
 * If this function throws, the caller MUST abort the lock entirely.
 * Never produce a locked PDF without a successfully generated appendix.
 *
 * This is the moat. Verbatim copy from OCOS-AppCopy.md §"PDF report — boilerplate copy".
 */

import { PDFDocument, rgb, StandardFonts } from 'pdf-lib';

// NAC brand colours
const DARK = rgb(8 / 255, 10 / 255, 7 / 255);       // #080A07
const GREEN = rgb(94 / 255, 232 / 255, 48 / 255);    // #5EE830
const AMBER = rgb(232 / 255, 168 / 255, 48 / 255);   // #E8A830

const PAGE_W = 595.28; // A4 width in points
const MARGIN = 40;
const CONTENT_W = PAGE_W - MARGIN * 2;

// Verbatim copy from OCOS-AppCopy.md
const PREAMBLE_BASE =
  'The records on this page document the photographic evidence, sign-offs, and regulatory ' +
  'references contained in this report. Each photograph carries a SHA-256 cryptographic hash ' +
  'computed at the moment of upload, before any processing. GPS coordinates and capture timestamps ' +
  'are recorded by the device. The hashes below allow third-party verification of the original evidence.\n\n' +
  'This document is a record-keeping aid generated by OCOS. It is not legal advice. Operators remain ' +
  'responsible for compliance with the Occupational Health and Safety Act and its regulations.';

const UPLOADED_DISCLAIMER =
  ' One or more photographs in this report were uploaded from the device library rather than captured ' +
  'live in OCOS. Original capture metadata is preserved from EXIF where present.';

const FOOTER_LINE =
  'Generated by OCOS — Ontario Compliance Operating System · naturalalternatives.ca';

/**
 * Append the Evidence & Audit Trail page(s) to an existing PDFDocument.
 *
 * @param {PDFDocument} pdfDoc   — the document being built
 * @param {object} auditData     — all evidence to include (see below)
 * @throws                       — throws if any required field is missing
 */
export async function appendAuditTrail(pdfDoc, auditData) {
  const {
    report,   // { id, version, generatedAt, generatedBy, licenseId }
    site,     // { id, name, geocode, siteType }
    photos,   // [{ photoId, capturedAt, capturedAtLocal, geoLat, geoLng, deviceIdHash, sha256, tags, ohsaRefs }]
    signoffs, // [{ workerId, role, method, timestamp, ipPrefix }]
    regulatoryAnchors, // string[] — every unique ref in the report
    documentHash,      // sha256 of the PDF bytes (set to '' before final hash; updated after)
    licenseeName,
  } = auditData;

  // Hard validation — fail loudly rather than silently omit
  if (!report?.id) throw new Error('AUDIT_APPENDIX: missing report.id');
  if (!site?.id) throw new Error('AUDIT_APPENDIX: missing site.id');
  if (!Array.isArray(photos)) throw new Error('AUDIT_APPENDIX: photos must be array');
  if (!Array.isArray(regulatoryAnchors)) throw new Error('AUDIT_APPENDIX: regulatoryAnchors must be array');

  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const fontBold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
  const fontMono = await pdfDoc.embedFont(StandardFonts.Courier);

  // Renderer state
  let page = pdfDoc.addPage([PAGE_W, 841.89]); // A4
  let y = 841.89 - MARGIN;

  function ensureSpace(needed) {
    if (y - needed < MARGIN + 30) {
      page = pdfDoc.addPage([PAGE_W, 841.89]);
      y = 841.89 - MARGIN;
    }
  }

  function drawText(text, { x = MARGIN, size = 8, color = DARK, f = font, maxW = CONTENT_W } = {}) {
    const lines = wrapText(text, f, size, maxW);
    for (const line of lines) {
      ensureSpace(size + 4);
      page.drawText(line, { x, y, size, font: f, color });
      y -= size + 3;
    }
  }

  function drawHRule(color = GREEN) {
    ensureSpace(8);
    page.drawLine({
      start: { x: MARGIN, y },
      end: { x: PAGE_W - MARGIN, y },
      thickness: 0.75,
      color,
    });
    y -= 8;
  }

  function drawSectionHeader(title) {
    ensureSpace(20);
    y -= 4;
    page.drawRectangle({ x: MARGIN, y: y - 2, width: CONTENT_W, height: 14, color: DARK });
    page.drawText(title.toUpperCase(), { x: MARGIN + 4, y: y, size: 9, font: fontBold, color: GREEN });
    y -= 18;
  }

  // ── Section divider header ────────────────────────────────────────────────
  ensureSpace(30);
  page.drawRectangle({ x: MARGIN, y: y - 4, width: CONTENT_W, height: 22, color: DARK });
  page.drawText('EVIDENCE & AUDIT TRAIL', {
    x: MARGIN + 6, y: y + 2, size: 13, font: fontBold, color: GREEN,
  });
  y -= 30;

  // ── Preamble ─────────────────────────────────────────────────────────────
  const hasUploadedPhotos = photos.some(p => (p.captureSource || '').startsWith('Uploaded'));
  const preamble = hasUploadedPhotos ? PREAMBLE_BASE + UPLOADED_DISCLAIMER : PREAMBLE_BASE;
  y -= 4;
  drawText(preamble, { size: 7.5, color: DARK });
  y -= 4;
  drawHRule();

  // ── Report metadata ───────────────────────────────────────────────────────
  drawSectionHeader('Report Metadata');
  const truncLicense = (report.licenseId || '').slice(0, 8) + '…';
  const reportMeta = [
    `Report ID: ${report.id}`,
    `Version: ${report.version || 1}`,
    `Generated At: ${report.generatedAt}`,
    `Generated By: ${report.generatedBy || '—'}`,
    `License ID: ${truncLicense}`,
  ];
  reportMeta.forEach(line => drawText(line, { size: 7.5, f: fontMono }));
  y -= 4;

  // ── Site metadata ─────────────────────────────────────────────────────────
  drawSectionHeader('Site Metadata');
  const siteMeta = [
    `Site ID: ${site.id}`,
    `Site Name: ${site.name}`,
    `Geocode: ${site.geocode || '—'}`,
    `Site Type: ${site.siteType || '—'}`,
  ];
  siteMeta.forEach(line => drawText(line, { size: 7.5, f: fontMono }));
  y -= 4;

  // ── Photo manifest ────────────────────────────────────────────────────────
  drawSectionHeader(`Photo Manifest (${photos.length} photo${photos.length !== 1 ? 's' : ''})`);
  for (const ph of photos) {
    ensureSpace(40);
    y -= 2;
    drawText(`Photo ID: ${ph.photoId}`, { size: 7, f: fontBold });
    drawText(`  Captured (UTC): ${ph.capturedAt || '—'}  |  Local: ${ph.capturedAtLocal || '—'}`, { size: 7, f: fontMono });
    const lat = typeof ph.geoLat === 'number' ? ph.geoLat.toFixed(5) : '—';
    const lng = typeof ph.geoLng === 'number' ? ph.geoLng.toFixed(5) : '—';
    drawText(`  GPS: ${lat}, ${lng}`, { size: 7, f: fontMono });
    const deviceTrunc = (ph.deviceIdHash || '').slice(0, 12) + (ph.deviceIdHash?.length > 12 ? '…' : '');
    drawText(`  Device: ${deviceTrunc}`, { size: 7, f: fontMono });
    drawText(`  SHA-256: ${ph.sha256 || '—'}`, { size: 6.5, f: fontMono });
    drawText(`  Tags: ${(ph.tags || []).join(', ') || '—'}`, { size: 7 });
    drawText(`  OHSA Refs: ${ph.ohsaRefs || '—'}`, { size: 7 });
    if (ph.captureSource && ph.captureSource !== 'Live') {
      drawText(`  ⚑ Capture Source: ${ph.captureSource}`, { size: 7, color: AMBER });
    }
    y -= 2;
  }

  // ── Sign-off manifest ─────────────────────────────────────────────────────
  drawSectionHeader(`Sign-off Manifest (${signoffs.length} signature${signoffs.length !== 1 ? 's' : ''})`);
  for (const sig of signoffs) {
    ensureSpace(24);
    y -= 2;
    drawText(`Worker ID: ${sig.workerId}  |  Role: ${sig.role}  |  Method: ${sig.method}`, { size: 7, f: fontMono });
    drawText(`  Timestamp: ${sig.timestamp}  |  IP prefix: ${sig.ipPrefix || '—'}`, { size: 7, f: fontMono });
    y -= 2;
  }

  // ── Regulatory anchors ────────────────────────────────────────────────────
  drawSectionHeader('Regulatory Anchors');
  if (regulatoryAnchors.length === 0) {
    drawText('None explicitly cited in this report.', { size: 7.5 });
  } else {
    regulatoryAnchors.forEach(ref => drawText(`• ${ref}`, { size: 7.5 }));
  }
  y -= 4;

  // ── Verification statement ────────────────────────────────────────────────
  drawSectionHeader('Verification');
  const verificationStatement =
    `This document was generated by OCOS on behalf of ${licenseeName || '[Licensee]'}. ` +
    'The document hash below was computed at the moment of locking and corresponds to the bytes of this PDF as delivered.';
  drawText(verificationStatement, { size: 7.5 });
  y -= 4;
  drawText(`Document SHA-256: ${documentHash || '[computed at lock time]'}`, { size: 7, f: fontMono });

  // ── Footer — MANDATORY on every report, even white-labelled ─────────────
  y -= 8;
  drawHRule(AMBER);
  drawText(FOOTER_LINE, { size: 7, color: AMBER, f: fontBold });
}

function wrapText(text, font, size, maxWidth) {
  const lines = [];
  for (const paragraph of text.split('\n')) {
    const words = paragraph.split(' ');
    let line = '';
    for (const word of words) {
      const test = line ? `${line} ${word}` : word;
      if (font.widthOfTextAtSize(test, size) <= maxWidth) {
        line = test;
      } else {
        if (line) lines.push(line);
        line = word;
      }
    }
    if (line) lines.push(line);
    if (paragraph === '') lines.push('');
  }
  return lines;
}
